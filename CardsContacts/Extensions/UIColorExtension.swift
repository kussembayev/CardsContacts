import UIKit

extension UIColor {

    class func navBarLeft() -> UIColor {
        return UIColor(colorLiteralRed: 247/255, green: 112/255, blue: 98/255, alpha: 1.0)
    }

    class func navBarRight() -> UIColor {
        return UIColor(colorLiteralRed: 254/255, green: 81/255, blue: 150/255, alpha: 1.0)
    }

    class func navBarMiddle() -> UIColor {
        return UIColor(colorLiteralRed: 250/255, green: 97/255, blue: 124/255, alpha: 1.0)
    }

    class func tabBar() -> UIColor {
        return UIColor.white
    }

    class func bg() -> UIColor {
        return UIColor.white
    }

}
